#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;
	size_t k;

	i = 0;
	k = ft_strlen(s1);
	while (s2[i] != '\0' && i != n)
	{
		s1[k] = s2[i];
		k++;
		i++;
	}
	s1[k] = '\0';
	return (s1);
}
